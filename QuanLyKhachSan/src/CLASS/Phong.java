
package CLASS;

public class Phong {
    String phong,ten,trangthai,idloaiphong,tang,soluongnguoi;

    public Phong() {
    }

    public Phong(String phong, String ten, String trangthai, String idloaiphong, String tang, String soluongnguoi) {
        this.phong = phong;
        this.ten = ten;
        this.trangthai = trangthai;
        this.idloaiphong = idloaiphong;
        this.tang = tang;
        this.soluongnguoi = soluongnguoi;
    }

    public void setPhong(String phong) {
        this.phong = phong;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public void setTrangthai(String trangthai) {
        this.trangthai = trangthai;
    }

    public void setIdloaiphong(String idloaiphong) {
        this.idloaiphong = idloaiphong;
    }

    public void setTang(String tang) {
        this.tang = tang;
    }

    public void setSoluongnguoi(String soluongnguoi) {
        this.soluongnguoi = soluongnguoi;
    }

    public String getPhong() {
        return phong;
    }

    public String getTen() {
        return ten;
    }

    public String getTrangthai() {
        return trangthai;
    }

    public String getIdloaiphong() {
        return idloaiphong;
    }

    public String getTang() {
        return tang;
    }

    public String getSoluongnguoi() {
        return soluongnguoi;
    }

 

    
}
